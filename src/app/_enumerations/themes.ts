/** Work for application switch UI theme based on settings. Such as logon user's prefer theme. */
export enum Themes {
    /** Telerik Kendo UI Angular UI default theme. */
    kendoDefault = 'KendoDefault',
    /** Telerik Kendo UI Angular UI customized theme. Blue color. */
    KendoBlue = 'KendoBlue',
    /** Telerik Kendo UI Angular Material UI. Not applied yet. */
    KendoMaterial = 'KendoMaterial',
    /** Google default material component. Not applied yet. */
    Material = 'Material'
}
